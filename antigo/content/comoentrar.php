<div id="title">Como entrar?</div>

<div class="cleaner2"></div>

<p>Podem candidatar-se à Tuna Académica todos os estudantes do sexo masculino devidamente inscritos num dos cursos leccionados na Universidade Fernando Pessoa (Porto).</p>
<p>Os candidatos poderão candidatar-se apresentando-se na sala da Tuna, situada no edifício principal (Praça 9 de Abril, 349), nos dias dos ensaios.</p>
<p>Ensaios (2014/ 2015):<br>
Segundas-feiras e Quartas-feiras às 21:00h na sala da Tuna.</p>
<p>Encontro:<br>
Junto ao bar da UFP (Sede).</p>


